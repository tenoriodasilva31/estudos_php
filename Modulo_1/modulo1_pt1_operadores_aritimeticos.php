<?php

/*
O operador "%" - operador de modulo, retorna o resto de uma divisão inteira  
*/

$var1 =  11;
$var2 = 2;

$modulo = $var1 % $var2;

echo "Resultado do módulo: " . $modulo;

echo "\n\n";

//Exponencial 


$var4 = 10;
$var5 =  2;

$exponencial = $var4 ** $var5;

echo "Resultado do exponencial: " . $exponencial;

echo "\n\n";
die;

